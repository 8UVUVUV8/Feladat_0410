public class Szamit {
    public static double szamit(int[]szam1){
        double eredmeny;
        double szam1Double = (double)szam1[0];
        double szam2Double = (double)szam1[1];


        if(szam1Double>szam2Double){
            eredmeny=szam1Double/szam2Double;
        }else{
            eredmeny=szam1Double/szam2Double;
        }
        return eredmeny;
    }
}
